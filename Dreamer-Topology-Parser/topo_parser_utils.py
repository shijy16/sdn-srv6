class Subnet: 
	def __init__(self, Type=None):
		self.nodes = []
		self.links = []

	def appendLink(self, link):
		if link[0] not in self.nodes:
			self.nodes.append(link[0])
		if link[1] not in self.nodes:
			self.nodes.append(link[1])
		self.links.append(link)
